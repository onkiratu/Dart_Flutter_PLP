void main() {
  // Integers represent whole numbers
  int age = 25;

  // Doubles represent floating-point numbers
  double height = 5.11;

  // Strings represent text data
  String name = "John Doe";

  // Lists represent ordered collections of objects
  List<String> hobbies = ["Reading", "Hiking", "Cooking"];

  // Maps represent key-value pairs
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'isStudent': true,
  };

  // Print statements to demonstrate each data type
  print("Age: $age"); // Output: Age: 25
  print("Height: $height"); // Output: Height: 5.11
  print("Name: $name"); // Output: Name: John Doe
  print("Hobbies: $hobbies"); // Output: Hobbies: [Reading, Hiking, Cooking]
  print(
      "Person: $person"); // Output: Person: {name: Alice, age: 30, isStudent: true}
}
